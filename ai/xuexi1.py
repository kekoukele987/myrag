from sklearn.linear_model import LogisticRegression
from sklearn.feature_extraction.text import TfidfVectorizer

# 1. 数据集：X=文本特征，Y=标签（0负面 / 1正面）
texts = [
    "游戏画面超棒，很好玩",
    "操作丝滑，强烈推荐",
    "卡顿严重，体验很差",
    "剧情无聊，不推荐"
]
labels = [1, 1, 0, 0]  # 监督学习必备标签

# 文本转数值特征
vec = TfidfVectorizer()
X = vec.fit_transform(texts)

# 2. 训练逻辑回归（监督算法）
model = LogisticRegression()
model.fit(X, labels)

# 3. 预测新文本
test_text = ["这款游戏非常垃圾"]
test_x = vec.transform(test_text)
pred = model.predict(test_x)
print("监督学习预测结果（0=负面，1=正面）：", pred[0])

"""
定义：训练数据同时包含「特征 X」和标签 Y，模型学习 X→Y 的映射关系，用于预测 / 分类。
两类任务：
回归：Y 是连续数值（房价、销量预测）
分类：Y 是离散类别（图片猫狗、文本正负情感）
代表算法：线性回归、逻辑回归、SVM、决策树、随机森林

"""


# =============================================================================
# 知识点总结：监督学习的实现分析
# =============================================================================
#
# 1. 监督学习的核心要素（三要素缺一不可）
#    · 特征 X（Feature）：模型的输入，描述样本的属性
#    · 标签 Y（Label）：模型的输出目标，人工标注的"正确答案"
#    · 映射关系 f(X)→Y：模型从数据中学到的函数
#
# 2. 本文件的监督学习五步流程
#
#    第一步：准备标注数据（监督的关键！）
#    ------------------------------------------------------------
#    texts  = ["游戏画面超棒", "操作丝滑", "卡顿严重", "剧情无聊"]
#    labels = [1, 1, 0, 0]   ← 每条数据必须有对应的标签
#
#    这就是"监督"的含义：每个训练样本都被人工标注了正确答案。
#    模型知道"游戏画面超棒"应该输出 1（正面），"卡顿严重"应该输出 0（负面）。
#
#    第二步：特征工程 — 文本 → 数值向量
#    ------------------------------------------------------------
#    TfidfVectorizer 将中文文本转换为机器可计算的数值特征矩阵。
#    TF-IDF = 词频 × 逆文档频率：
#      - TF（Term Frequency）：某个词在一条文本中出现越多，越重要
#      - IDF（Inverse Document Frequency）：某词在所有文本中都出现，
#        说明它区分度不高（如"的""了"），权重降低
#    fit_transform(texts)：同时完成"学习词表"和"转换文本"两步
#    transform(test_text)：用已学词表转换新文本（不能再用 fit！）
#
#    第三步：选择算法 — 逻辑回归
#    ------------------------------------------------------------
#    LogisticRegression 是经典二分类算法，尽管名字带"回归"。
#    内部原理：
#      - 线性部分：z = w₁x₁ + w₂x₂ + ... + wₙxₙ + b（加权求和）
#      - Sigmoid 压缩：σ(z) = 1/(1+e⁻ᶻ)，把任意实数压缩到 (0,1) 区间
#      - 输出解释为"正类的概率"
#      - 决策边界：概率 ≥ 0.5 判正类，< 0.5 判负类
#
#    第四步：训练 — model.fit(X, labels)
#    ------------------------------------------------------------
#    这是监督学习的核心动作。fit() 内部做的事：
#      - 初始化权重 w 和偏置 b（通常随机或全零）
#      - 用训练数据反复计算"预测值 vs 真实标签"的误差（交叉熵损失）
#      - 用梯度下降更新 w 和 b，让误差越来越小
#      - 迭代直到收敛或达到最大轮数
#    最终模型"记住"了什么样的词组合 → 正面，什么样的 → 负面。
#
#    第五步：预测 — model.predict(test_x)
#    ------------------------------------------------------------
#    用训练好的权重，对新文本做一次正向计算，输出预测类别。
#    新文本"这款游戏非常垃圾"从未在训练集中出现过，
#    但模型能泛化判断它为负面，因为它学到了"垃圾"≈ 负面。
#
# 3. TF-IDF 特征工程的深入理解
#    · 为什么不能直接把文字喂给模型？模型只懂数字。
#    · fit_transform() vs transform()：
#      - fit：扫描所有训练文本，建立词表（词汇 → 列索引 的映射）
#      - transform：按照已有词表，把文本转成向量
#      - 训练时用 fit_transform，预测时只能 transform（词表必须一致！）
#    · 输出是稀疏矩阵（大部分元素为 0），节省内存。
#
# 4. 逻辑回归的数学直觉
#    · 它学习的是一根"分界线"（决策边界），把正负样本分开
#    · 对每个词赋予一个权重 wᵢ：
#      - wᵢ > 0：这个词偏向正面（如"推荐"权重为正）
#      - wᵢ < 0：这个词偏向负面（如"垃圾"权重为负）
#      - wᵢ ≈ 0：这个词对情感判断没什么帮助
#    · 最终判断：所有词权重求和 → 正数判正面，负数判负面
#
# 5. 监督 vs 无监督 vs 半监督
#    · 监督学习（本文件）：有标签，学 X→Y 映射
#    · 无监督学习：无标签，学数据本身的结构（聚类、降维）
#    · 半监督学习：少量有标签 + 大量无标签数据
#    · 自监督学习：从数据自身构造伪标签（如 BERT 的 MLM 预训练）
#
# 6. 本例的局限性
#    · 训练数据极少（仅 4 条），模型泛化能力有限
#    · TF-IDF 只看词频，不懂词序和语义（如"不 错"和"错"区分不好）
#    · 逻辑回归是线性模型，无法捕捉复杂的非线性关系
#    · 改进方向：更多数据、Word2Vec/BERT 语义向量、XGBoost/深度学习
#
# 7. 实际应用场景
#    · 情感分析：电商评论正负面判断
#    · 垃圾邮件识别：邮件 → 垃圾/正常
#    · 信用评估：用户信息 → 是否违约
#    · 医疗诊断：症状数据 → 是否患病
# =============================================================================
